package codingPractice;

public class swapNumber {
    public static void main(String[]args){
        //logic 1 third variable
        int a=10;
        int b=20;
        System.out.println("Before Swaping "+a +" "+b);
        int t=a;
        a=b;
        b=t;
        System.out.println("After Swaping "+a +" "+b);
    }
}
